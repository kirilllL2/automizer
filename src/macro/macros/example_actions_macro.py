"""
Пример макроса с использованием модуля actions.

Демонстрирует:
- Клик в определенную координату
- Клик в случайное место в области
- Распознавание области по пресету
- Поиск области в ограниченной зоне
"""

# Метаданные макроса
NAME = "Пример действий с кликами и CV"
DESCRIPTION = "Демонстрация кликов и распознавания областей"

# Импортируем функции из модуля actions
# В реальном макросе используйте: from src.macro import *
from src.macro.actions import (
    Region,
    click,
    click_in_region,
    find_region,
    find_all_regions,
    wait_for_region,
    create_region_from_center,
)
import time


def delay(seconds: float):
    """Выполняет задержку на указанное количество секунд."""
    print(f"[Action] Задержка на {seconds} сек.")
    time.sleep(seconds)


def run():
    """
    Основная функция макроса.
    Демонстрирует различные способы кликов и распознавания областей.
    """
    print("=== Начало выполнения макроса ===\n")
    
    # =========================================================================
    # 1. Клик в определенную координату
    # =========================================================================
    print("1. Клик в точку (500, 300)")
    click(500, 300)
    delay(0.5)
    
    # =========================================================================
    # 2. Клик в случайное место в области (вся область)
    # =========================================================================
    print("\n2. Клик в случайное место в области (100, 100) - (300, 200)")
    region = Region(x1=100, y1=100, x2=300, y2=200)
    print(f"   Область: {region}")
    print(f"   Центр: {region.center}")
    click_in_region(region, percent=0.0)  # 0% = вся область
    delay(0.5)
    
    # =========================================================================
    # 3. Клик в случайное место в области (50% от центра)
    # =========================================================================
    print("\n3. Клик в случайное место в 50% от центра области")
    click_in_region(region, percent=0.5)  # 50% = половина области от центра
    delay(0.5)
    
    # =========================================================================
    # 4. Клик в случайное место в области (90% от центра - почти центр)
    # =========================================================================
    print("\n4. Клик в случайное место в 90% от центра области (почти центр)")
    click_in_region(region, percent=0.9)  # 90% = маленькая область вокруг центра
    delay(0.5)
    
    # =========================================================================
    # 5. Клик с гауссовым распределением (больше кликов ближе к центру)
    # =========================================================================
    print("\n5. Клик с гауссовым распределением (центр области)")
    click_in_region(region, percent=0.5, distribution="gaussian")
    delay(0.5)
    
    # =========================================================================
    # 6. Распознавание области по пресету (по всему экрану)
    # =========================================================================
    print("\n6. Поиск области по пресету 'my_button' (по всему экрану)")
    try:
        found_region = find_region(
            preset_id="my_button",
            confidence=0.8  # Можно переопределить значение из конфига
        )
        if found_region:
            print(f"   Найдена область: {found_region}")
            # Кликаем в центр найденной области
            cx, cy = found_region.center
            click(cx, cy)
        else:
            print("   Область не найдена (это нормально для демонстрации)")
    except FileNotFoundError as e:
        print(f"   Пресет не найден: {e}")
    delay(0.5)
    
    # =========================================================================
    # 7. Распознавание области по пресету в ограниченной зоне
    # =========================================================================
    print("\n7. Поиск области по пресету в ограниченной зоне")
    search_region = Region(x1=0, y1=0, x2=400, y2=300)
    print(f"   Область поиска: {search_region}")
    try:
        found_region = find_region(
            preset_id="my_button",
            confidence=0.8,
            search_region=search_region  # Ищем только в этой области
        )
        if found_region:
            print(f"   Найдена область: {found_region}")
            click_in_region(found_region, percent=0.3)
        else:
            print("   Область не найдена в указанной зоне")
    except FileNotFoundError as e:
        print(f"   Пресет не найден: {e}")
    delay(0.5)
    
    # =========================================================================
    # 8. Ожидание появления области с таймаутом
    # =========================================================================
    print("\n8. Ожидание появления области (таймаут 5 секунд)")
    try:
        found_region = wait_for_region(
            preset_id="my_button",
            timeout=5.0,
            interval=0.5,
            confidence=0.8
        )
        if found_region:
            print(f"   Область появилась: {found_region}")
            click_in_region(found_region, percent=0.0)
        else:
            print("   Таймаут ожидания истек")
    except FileNotFoundError as e:
        print(f"   Пресет не найден: {e}")
    delay(0.5)
    
    # =========================================================================
    # 9. Создание области от центра
    # =========================================================================
    print("\n9. Создание области от центра и клик в неё")
    center_region = create_region_from_center(
        center_x=600,
        center_y=400,
        width=200,
        height=150
    )
    print(f"   Создана область: {center_region}")
    click_in_region(center_region, percent=0.2)
    delay(0.5)
    
    # =========================================================================
    # 10. Поиск всех совпадений пресета
    # =========================================================================
    print("\n10. Поиск всех областей по пресету")
    try:
        all_regions = find_all_regions(
            preset_id="my_button",
            confidence=0.8,
            max_results=5
        )
        print(f"   Найдено областей: {len(all_regions)}")
        for i, reg in enumerate(all_regions, 1):
            print(f"      {i}. {reg}")
    except FileNotFoundError as e:
        print(f"   Пресет не найден: {e}")
    
    print("\n=== Макрос завершен ===")


if __name__ == "__main__":
    # Для тестирования можно запустить напрямую
    run()
