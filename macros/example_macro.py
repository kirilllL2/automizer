# Метаданные макроса (отображаются в UI)
NAME = "Пример макроса"
DESCRIPTION = "Простой пример: клик и задержка"

# Импортируем вспомогательные функции из модуля macro
# Для использования в Windows раскомментируйте импорт ниже:
# from src.macro import click, delay

# Для тестирования без Windows используем заглушки:
def click(x: int, y: int):
    print(f"[Action] Клик в точке ({x}, {y})")

def delay(seconds: float):
    import time
    print(f"[Action] Задержка на {seconds} сек.")
    time.sleep(seconds)


def run():
    """
    Основная функция макроса.
    Вызывается при запуске макроса из UI.
    """
    # Пример последовательности действий
    print("Начало выполнения макроса")
    
    # Клик в точку (100, 100)
    click(100, 100)
    
    # Задержка 1 секунда
    delay(1.0)
    
    # Клик в точку (200, 200)
    click(200, 200)
    
    # Задержка 0.5 секунды
    delay(0.5)
    
    print("Макрос завершен")
